function run(...param) {
  const fellowship = param;
  let [var1, var2, var3, var4, var5] = fellowship;
  console.log(var1);  //Lorem
  console.log(var2);  //ipsum
  console.log(var3);  //dolor
  console.log(var4);  //sit
  console.log(var5);  //amet
};

run('Lorem', 'ipsum', 'dolor', 'sit', 'amet');